    private void TablasestudiantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablasestudiantesMouseClicked
        // TODO add your handling code here:
            if (evt.getClickCount() == 2) { 
            int fila = Tablasestudiantes.rowAtPoint(evt.getPoint());
            if (fila >= 0) {
                Estudiante seleccionado = listaEstudiantes.get(fila); // ‚Üê ya no por nombre
                JCalificacionEspecifica califica = new JCalificacionEspecifica(seleccionado, mate);
                califica.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Hubo un error. Intente de nuevo.");
            }
        }

    }//GEN-LAST:event_TablasestudiantesMouseClicked

